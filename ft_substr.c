#include "libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	size_t	i;
	size_t	tmp;
	char	*str;

	i = 0;
	if (start >= ft_strlen(s))
		return (ft_strdup(""));
	tmp = ft_strlen(s) - start;
	if (len > tmp)
		len = tmp;
	str = (char *)malloc(sizeof(char) * (len + 1));
	if (!str)
		return (NULL);
	while (i < len)
	{
		str[i] = s[start + i];
		i++;
	}
	str[i] = 0;
	return (str);
}
